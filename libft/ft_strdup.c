#include "libft.h"

char	*ft_strdup(char *src)
{
	char	*str;
	int		i;
	int		size;

	i = 0;
	size = ft_strlen(src);
	str = (char*)malloc(sizeof(char) * (size + 1));
	if (!str)
		return (NULL);
	while (src[i] != '\0')
	{
		str[i] = src[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
